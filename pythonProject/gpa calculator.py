number = int(input("Enter number: "))
if number >= 80:
    print("you got A+ grade")

elif number >= 70:
    print("You got A grade")
elif number >= 60:
    print("You got A- grade")
elif number >= 50:
    print("You got B grade")
elif number >= 40:
    print("you got C grade")
elif number >= 30:
    print("You got D grade")
else:
    print("You are fail")